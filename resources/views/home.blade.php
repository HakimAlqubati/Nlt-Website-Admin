    <script>
        gsap.registerPlugin(ScrollTrigger, SplitText, CustomEase);
        window.addEventListener("DOMContentLoaded", () => {
            document.fonts.ready.then(() => {
                // Button animation
                function createButton(buttonElement) {
                    const el = gsap.utils.selector(buttonElement);
                    const DOM = {
                        button: buttonElement,
                        flair: el(".button-flair")
                    };
                    const xSet = gsap.quickSetter(DOM.flair, "xPercent");
                    const ySet = gsap.quickSetter(DOM.flair, "yPercent");

                    function getXY(e) {
                        const {
                            left,
                            top,
                            width,
                            height
                        } = DOM.button.getBoundingClientRect();
                        const xTransformer = gsap.utils.pipe(
                            gsap.utils.mapRange(0, width, 0, 100),
                            gsap.utils.clamp(0, 100)
                        );
                        const yTransformer = gsap.utils.pipe(
                            gsap.utils.mapRange(0, height, 0, 100),
                            gsap.utils.clamp(0, 100)
                        );
                        return {
                            x: xTransformer(e.clientX - left),
                            y: yTransformer(e.clientY - top)
                        };
                    }

                    function onMouseEnter(e) {
                        const {
                            x,
                            y
                        } = getXY(e);
                        xSet(x);
                        ySet(y);
                        gsap.to(DOM.flair, {
                            scale: 1,
                            duration: 0.4,
                            ease: "power2.out"
                        });
                    }

                    function onMouseLeave(e) {
                        const {
                            x,
                            y
                        } = getXY(e);
                        gsap.killTweensOf(DOM.flair);
                        gsap.to(DOM.flair, {
                            xPercent: x > 90 ? x + 20 : x < 10 ? x - 20 : x,
                            yPercent: y > 90 ? y + 20 : y < 10 ? y - 20 : y,
                            scale: 0,
                            duration: 0.3,
                            ease: "power2.out"
                        });
                    }

                    function onMouseMove(e) {
                        const {
                            x,
                            y
                        } = getXY(e);
                        gsap.to(DOM.flair, {
                            xPercent: x,
                            yPercent: y,
                            duration: 0.4,
                            ease: "power2"
                        });
                    }
                    DOM.button.addEventListener("mouseenter", onMouseEnter);
                    DOM.button.addEventListener("mouseleave", onMouseLeave);
                    DOM.button.addEventListener("mousemove", onMouseMove);
                }
                document.querySelectorAll('[data-block="button"]').forEach(createButton);
                // Menu hover animation
                document.querySelectorAll("[menu]").forEach((item) => {
                    const split = new SplitText(item, {
                        type: "chars"
                    });
                    const chars = split.chars;
                    gsap.set(chars, {
                        y: 0,
                        opacity: 1,
                        display: "inline-block"
                    });
                    item.style.cursor = "pointer";
                    item.style.display = "inline-block";
                    item.style.overflow = "hidden";
                    item.addEventListener("mouseenter", () => {
                        gsap.fromTo(
                            chars, {
                                y: "70%",
                                opacity: 0
                            }, {
                                y: "0%",
                                opacity: 1,
                                duration: 0.4,
                                stagger: 0.05,
                                ease: "power3.out"
                            }
                        );
                    });
                });
                // Text animation on scroll
                const headings = document.querySelectorAll('[data-animation="chars"]');
                const paragraphs = document.querySelectorAll('[data-animation="lines"]');
                let splitHeadingInstances = [];
                let splitParagraphInstances = [];

                function createSplits() {
                    splitHeadingInstances.forEach((split) => split.revert());
                    splitParagraphInstances.forEach((split) => split.revert());
                    splitHeadingInstances = [];
                    splitParagraphInstances = [];
                    headings.forEach((heading) => {
                        const split = new SplitText(heading, {
                            type: "lines,words,chars",
                            mask: heading.getAttribute("data-animation-mask") || "chars",
                            tag: "span",
                            linesClass: "split-line",
                            wordsClass: "split-word",
                            charsClass: "split-char"
                        });
                        splitHeadingInstances.push(split);
                        heading.style.opacity = "1";
                        gsap.from(split.chars, {
                            scrollTrigger: {
                                trigger: heading,
                                start: heading.getAttribute("data-animation-start") ||
                                    "top 80%",
                                end: heading.getAttribute("data-animation-end") ||
                                    "100% top",
                                toggleActions: heading.getAttribute(
                                    "data-animation-actions") || "play none none none"
                            },
                            y: heading.getAttribute("data-animation-y") || "1.5em",
                            x: heading.getAttribute("data-animation-x") || "0",
                            opacity: heading.getAttribute("data-animation-opacity") || 1,
                            duration: heading.getAttribute("data-animation-duration") || 1,
                            delay: heading.getAttribute("data-animation-delay") || 0.5,
                            ease: heading.getAttribute("data-animation-ease") || CustomEase
                                .create("custom", "M0,0 C0.047,0.752 0.082,1 1,1 "),
                            stagger: heading.getAttribute("data-animation-stagger") || 0.02
                        });
                    });
                    paragraphs.forEach((paragraph) => {
                        const splitParagraphs = new SplitText(paragraph, {
                            type: "lines",
                            mask: paragraph.getAttribute("data-animation-mask") || "lines",
                            tag: "span",
                            linesClass: "split-line"
                        });
                        splitParagraphInstances.push(splitParagraphs);
                        paragraph.style.opacity = "1";
                        gsap.from(splitParagraphs.lines, {
                            scrollTrigger: {
                                trigger: paragraph,
                                start: paragraph.getAttribute("data-animation-start") ||
                                    "top 80%",
                                toggleActions: paragraph.getAttribute(
                                    "data-animation-actions") || "play none none none"
                            },
                            y: paragraph.getAttribute("data-animation-y") || "2em",
                            x: paragraph.getAttribute("data-animation-x") || 0,
                            opacity: paragraph.getAttribute("data-animation-opacity") || 1,
                            duration: paragraph.getAttribute("data-animation-duration") ||
                                1,
                            delay: paragraph.getAttribute("data-animation-delay") || 0.5,
                            ease: paragraph.getAttribute("data-animation-ease") ||
                                CustomEase.create("custom", "M0,0 C0.102,0.667 0.2,1 1,1 "),
                            stagger: paragraph.getAttribute("data-animation-stagger") || 0.2
                        });
                    });
                }
                createSplits();
                let resizeTimeout;
                let lastWindowWidth = window.innerWidth;
                window.addEventListener("resize", () => {
                    const currentWindowWidth = window.innerWidth;
                    if (currentWindowWidth !== lastWindowWidth) {
                        clearTimeout(resizeTimeout);
                        resizeTimeout = setTimeout(() => {
                            createSplits();
                            lastWindowWidth = currentWindowWidth;
                        }, 250);
                    }
                });
            });
        });
    </script>
